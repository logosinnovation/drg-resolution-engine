package com.mmm.his.cer.foundation.exception;
public class FoundationException extends Exception {
    public FoundationException() { super(); }
    public FoundationException(String msg) { super(msg); }
    public FoundationException(String msg, Throwable cause) { super(msg, cause); }
    public FoundationException(Throwable cause) { super(cause); }
}
